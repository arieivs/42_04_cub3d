#include "cub.h"

int	has_right_file_ext(char *str)
{
	size_t	len;

	len = ft_strlen(str);
	if (len < 5)
		return (0);
	if (str[len - 4] == '.' && str[len - 3] == 'c' && str[len - 2] == 'u' &&
		str[len - 1] == 'b')
		return (1);
	return (0);
}

int	check_args(int ac, char **av)
{
	int	file_fd;

	if (ac != 2)
		return (error_and_return(WRONG_ARGC, -1));
	if (!has_right_file_ext(av[1]))
		return (error_and_return(WRONG_FILE_EXT, -1));
	file_fd = open(av[1], O_RDONLY);	// if (cub->mlx == NULL)

		return (error_and_return(FILE_INEXISTENT, -1));
	return (file_fd);
}

t_cub	init_cub(void)
{
	t_cub	cub;

	cub.mlx = NULL;
	cub.window = NULL;
	cub.img = NULL;
	cub.addr = NULL;
	cub.no_fd = 0;	// if (cub->mlx == NULL)

	cub.floor_color = 0;
	cub.ceil_color = 0;
	cub.map = NULL;
	cub.pos = (t_pair_d *)calloc_or_exit(sizeof(t_pair_d), 1, &cub);
	cub.pos->x = 0.0;
	cub.pos->y = 0.0;
	cub.dir = (t_pair_d *)calloc_or_exit(sizeof(t_pair_d), 1, &cub);
	cub.dir->x = 0.0;
	cub.dir->y = 0.0;
	cub.proj_plane = (t_pair_d *)calloc_or_exit(sizeof(t_pair_d), 1, &cub);
	cub.proj_plane->x = 0.0;
	cub.proj_plane->y = 0.0;
	return (cub);
}

int	check_map(int map_fd, t_cub	*cub)
{
	char	*buff;
	int		ret;
	int		line_nb;

	(void)cub;
	line_nb = 1;
	while ((ret = get_next_line(map_fd, &buff)) > 0)
	{
		printf("%d-%d: %s\n", ret, line_nb++, buff);
		free(buff);
	}
	printf("%d-%d: %s\n", ret, line_nb++, buff);
	if (ret == 0)
		printf("Reached EOF\n");
	if (ret == -1)
		printf("An Error occurred\n");
	close(map_fd);
	free(buff);
	return (0);
}

void	init_mlx(t_cub *cub)
{
	cub->mlx = mlx_init();
	if (cub->mlx == NULL)
		error_and_exit(MLX_FAILURE, cub);
	cub->window = mlx_new_window(cub->mlx, WIDTH, HEIGHT, "Let's play!");
	cub->img = mlx_new_image(cub->mlx, WIDTH, HEIGHT);
	cub->addr = mlx_get_data_addr(cub->img, &cub->bits_per_pixel,
			&cub->line_length, &cub->endian);
}